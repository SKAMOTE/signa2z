import 'package:flutter/material.dart';
import 'package:firebase_core/firebase_core.dart';
import 'firebase_options.dart'; // ğŸ‘ˆ generated by FlutterFire CLI
import 'package:signa2z/pages/welcomepage.dart';
import 'package:signa2z/pages/loginpage.dart';
import 'package:signa2z/pages/signuppage.dart';
import 'package:signa2z/pages/historypage.dart';

Future<void> main() async {
  WidgetsFlutterBinding.ensureInitialized();
  await Firebase.initializeApp(
    options: DefaultFirebaseOptions.currentPlatform, // ğŸ‘ˆ uses firebase_options.dart
  );
  runApp(const MyApp());
}

class MyApp extends StatelessWidget {
  const MyApp({super.key});

  @override
  Widget build(BuildContext context) {
    return MaterialApp(
      title: 'Signa2Z',
      debugShowCheckedModeBanner: false,
      theme: ThemeData(primarySwatch: Colors.blue),
      initialRoute: '/',
      routes: {
        '/': (context) => const Welcomepage(),
        '/login': (context) => const LoginPage(),
        '/signup': (context) => const SignupPage(),
        '/history': (context) => const HistoryPage(),
      },
    );
  }
}
